package com.company;

public class AssignVariables {

    public static void main(String[] args) {
	    Boolean bool = false;
        Byte byteNumber = 127;
        String address = "Palo Alto, CA";
        Short shortNumber = 32767;
        Integer intNumber = 2000000000;
        Double doubleNumber = 0.1234567891011;
        Float floatNumber = 0.5f;
        Long longNumber = 919827112351L;
        Character charLetter = 'c';

        System.out.println(bool);
        System.out.println(byteNumber);
        System.out.println(address);
        System.out.println(shortNumber);
        System.out.println(intNumber);
        System.out.println(doubleNumber);
        System.out.println(floatNumber);
        System.out.println(longNumber);
        System.out.println(charLetter);
    }
}
